#!/usr/bin/env python3
"""Generate the P1 access-semantics kernel matrix as LLVM IR (kaappi#1473).

Six numeric kernels (memo research/p1-access-semantics.md §9.2) x three element
access encodings (plain / unordered / monotonic). The three encodings of a
kernel are *mechanically identical* IR except for the atomic annotation on the
element load/store -- that is the whole experimental variable, so it must be the
only textual difference.

Faithfulness to KEP-0003's element-access lowering
--------------------------------------------------
KEP-0003's reference sketch says every `-ref`/`-set!` "compiles to a single
aligned load/store of the element width". Each kernel is exactly that: a counted
loop whose body is one `getelementptr <elemty>, ptr %base, i64 %i` followed by a
single `load`/`store` of the element width. The base pointer in the real backend
comes from unmasking a NaN-boxed buffer object and offsetting to its payload;
that is loop-invariant and hoisted, so it does not affect inner-loop
vectorization and is omitted here (the kernel takes the raw payload pointer as an
argument, which is the honest inner-loop model). The clean counted loop is the
*ceiling*: realizing it requires KEP-0003's lowering to present a counted loop
with hoisted bounds checks -- a normative implementation requirement the report
records, not an assumption this harness hides.

Encodings
---------
  plain      : `load T, ptr %p`                 / `store T %v, ptr %p`
  unordered  : `load atomic T, ptr %p unordered`/ `store atomic T %v, ptr %p unordered`
  monotonic  : `load atomic T, ptr %p monotonic`/ `store atomic T %v, ptr %p monotonic`

Each kernel file exports a uniform wrapper

    void kernel_run(ptr %out, ptr %in, i64 %n)

so a single driver drives all of them. Reduction kernels store their scalar
result through %out[0] so the optimizer cannot delete the loop as dead.

The target triple is deliberately omitted: `zig cc` fills in the host triple,
so the same generated file measures whichever of the two reference machines it
runs on.
"""

import os
import sys

# (id, element LLVM type, element align, uniform "kind") -- the kind selects the
# loop body template below.
KERNELS = [
    ("f64_fill", "double", 8, "fill"),
    ("f64_map", "double", 8, "map"),
    ("f64_sum", "double", 8, "sum_f64"),
    ("i64_checksum", "i64", 8, "sum_i64"),
    ("u8_fill", "i8", 1, "fill_u8"),
    ("u8_copy", "i8", 1, "copy_u8"),
]


def access(kind, encoding, ty, align):
    """Return (load_fmt, store_fmt) for the chosen encoding.

    load_fmt has one slot: the result SSA name and the pointer.
    store_fmt has two slots: the value and the pointer.
    """
    if encoding == "plain":
        ld = "  {dst} = load {ty}, ptr {ptr}, align {al}\n"
        st = "  store {ty} {val}, ptr {ptr}, align {al}\n"
    else:
        ld = "  {dst} = load atomic {ty}, ptr {ptr} " + encoding + ", align {al}\n"
        st = "  store atomic {ty} {val}, ptr {ptr} " + encoding + ", align {al}\n"
    return ld, st


def gen(kind, ty, align, encoding):
    ld, st = access(kind, encoding, ty, align)
    L = []
    L.append("; kernel=%s encoding=%s (generated by gen_kernels.py)" % (kind, encoding))
    L.append("define void @kernel_run(ptr %out, ptr %in, i64 %n) {")
    L.append("entry:")
    L.append("  %pos = icmp sgt i64 %n, 0")
    L.append("  br i1 %pos, label %loop, label %exit")
    L.append("loop:")

    if kind in ("fill", "fill_u8"):
        splat = "1.000000e+00" if ty == "double" else "-85"  # 0xAB as i8
        L.append("  %i = phi i64 [ 0, %entry ], [ %i.next, %loop ]")
        L.append("  %p = getelementptr {ty}, ptr %out, i64 %i".format(ty=ty))
        L.append(st.format(ty=ty, val=splat, ptr="%p", al=align).rstrip("\n"))
        L.append("  %i.next = add nuw nsw i64 %i, 1")
        L.append("  %done = icmp eq i64 %i.next, %n")
        L.append("  br i1 %done, label %exit, label %loop")
        L.append("exit:")
        L.append("  ret void")

    elif kind == "map":
        # out[i] = a*in[i] + b   (a=2.0, b=3.0)
        L.append("  %i = phi i64 [ 0, %entry ], [ %i.next, %loop ]")
        L.append("  %pin = getelementptr double, ptr %in, i64 %i")
        L.append(ld.format(dst="%x", ty=ty, ptr="%pin", al=align).rstrip("\n"))
        L.append("  %t = fmul double %x, 2.000000e+00")
        L.append("  %y = fadd double %t, 3.000000e+00")
        L.append("  %pout = getelementptr double, ptr %out, i64 %i")
        L.append(st.format(ty=ty, val="%y", ptr="%pout", al=align).rstrip("\n"))
        L.append("  %i.next = add nuw nsw i64 %i, 1")
        L.append("  %done = icmp eq i64 %i.next, %n")
        L.append("  br i1 %done, label %exit, label %loop")
        L.append("exit:")
        L.append("  ret void")

    elif kind == "copy_u8":
        L.append("  %i = phi i64 [ 0, %entry ], [ %i.next, %loop ]")
        L.append("  %pin = getelementptr i8, ptr %in, i64 %i")
        L.append(ld.format(dst="%x", ty=ty, ptr="%pin", al=align).rstrip("\n"))
        L.append("  %pout = getelementptr i8, ptr %out, i64 %i")
        L.append(st.format(ty=ty, val="%x", ptr="%pout", al=align).rstrip("\n"))
        L.append("  %i.next = add nuw nsw i64 %i, 1")
        L.append("  %done = icmp eq i64 %i.next, %n")
        L.append("  br i1 %done, label %exit, label %loop")
        L.append("exit:")
        L.append("  ret void")

    elif kind == "sum_f64":
        # Strict left-to-right reduction: NO fast-math flags, so no encoding
        # (plain included) may reassociate it into a vector reduction. This
        # kernel is the "drops out" witness of memo §9.2 -- informative only in
        # that it must show plain not vectorizing either.
        L.append("  %i = phi i64 [ 0, %entry ], [ %i.next, %loop ]")
        L.append("  %acc = phi double [ 0.000000e+00, %entry ], [ %acc.next, %loop ]")
        L.append("  %p = getelementptr double, ptr %in, i64 %i")
        L.append(ld.format(dst="%x", ty=ty, ptr="%p", al=align).rstrip("\n"))
        L.append("  %acc.next = fadd double %acc, %x")
        L.append("  %i.next = add nuw nsw i64 %i, 1")
        L.append("  %done = icmp eq i64 %i.next, %n")
        L.append("  br i1 %done, label %store, label %loop")
        L.append("store:")
        L.append("  store double %acc.next, ptr %out, align 8")
        L.append("  br label %exit")
        L.append("exit:")
        L.append("  ret void")

    elif kind == "sum_i64":
        # Integer reduction: reassociation is legal, so plain vectorizes.
        L.append("  %i = phi i64 [ 0, %entry ], [ %i.next, %loop ]")
        L.append("  %acc = phi i64 [ 0, %entry ], [ %acc.next, %loop ]")
        L.append("  %p = getelementptr i64, ptr %in, i64 %i")
        L.append(ld.format(dst="%x", ty=ty, ptr="%p", al=align).rstrip("\n"))
        L.append("  %acc.next = add i64 %acc, %x")
        L.append("  %i.next = add nuw nsw i64 %i, 1")
        L.append("  %done = icmp eq i64 %i.next, %n")
        L.append("  br i1 %done, label %store, label %loop")
        L.append("store:")
        L.append("  store i64 %acc.next, ptr %out, align 8")
        L.append("  br label %exit")
        L.append("exit:")
        L.append("  ret void")

    else:
        raise ValueError(kind)

    L.append("}")
    return "\n".join(L) + "\n"


def main():
    outdir = sys.argv[1] if len(sys.argv) > 1 else os.path.join(os.path.dirname(__file__), "kernels")
    os.makedirs(outdir, exist_ok=True)
    encodings = ["plain", "unordered", "monotonic"]
    for name, ty, align, kind in KERNELS:
        for enc in encodings:
            ir = gen(kind, ty, align, enc)
            path = os.path.join(outdir, "%s_%s.ll" % (name, enc))
            with open(path, "w") as fh:
                fh.write(ir)
    print("wrote %d kernel IR files to %s" % (len(KERNELS) * len(encodings), outdir))


if __name__ == "__main__":
    main()
